## Перебор массива с помощью `foreach`

Используется, когда важно просто получить значения, а не индекс.

```csharp runnable=true
int[] numbers = { 1, 2, 3, 4, 5 };
foreach (int number in numbers)
{
    Console.WriteLine(number);
}
```

## Перебор через `while`

Используется, если количество шагов заранее неизвестно или цикл зависит от внешних условий.

```csharp runnable=true
int[] numbers = { 1, 2, 3, 4, 5 };
int i = 0;
while (i < numbers.Length)
{
    Console.WriteLine(numbers[i]);
    i++;
}
```

## Перебор через `do...while`

Этот цикл выполнит тело как минимум один раз, даже если условие ложное.

```csharp runnable=true
int[] numbers = { 1, 2, 3, 4, 5 };
int j = 0;
do
{
    Console.WriteLine(numbers[j]);
    j++;
}
while (j < numbers.Length);
```

## Итого

- `for` — когда нужен контроль индекса
- `foreach` — проще всего и безопасно
- `while` — неизвестное число итераций
- `do...while` — нужно выполнить хотя бы один раз

